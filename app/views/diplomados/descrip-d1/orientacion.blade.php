@section('descripcion')
	El diplomado en  Análisis de Negocios, Procesos  y Requisitos  se orienta a la capacitación de profesionales, cuyo perfil es requerido por las gerencias de informática o las empresas que desarrollan software, para ejercer los siguientes cargos o roles:<br>
	<ul>
		<li>Analista de Negocio</li>
		<li>Analista de Procesos</li>
		<li>Analista de Requisitos</li>
		<li>Ingeniero de Requisitos</li>
	</ul><br>
	Este diplomado está orientado a desarrollar, en sus participantes, <strong>competencias específicas</strong> en estas tres disciplinas, basadas en la utilización de:
	<br><br>
	<ul>
		<li>Cuerpos de conocimientos reconocidos internacionalmente</li>
		<li>Marcos conceptuales y metodológicos</li>
		<li>Métodos y técnicas</li>
		<li>Lenguajes de modelado</li>
		<li>Mejores prácticas</li>
		<li>Herramientas de software</li>
	</ul><br>
	Entre las competencias que deben ser desarrolladas por estos profesionales para ejercer con éxito estos roles están:<br><br>
	<ul>
		<li>Planificar y controlar las actividades de análisis de negocios, gestión de procesos de negocio e ingeniería de requisitos.</li>
		<li>Identificar, describir, modelar, analizar, relacionar y documentar los elementos de una organización o sistema de negocio. Estos elementos incluyen: objetivos, procesos de negocio, actividades, servicios, recursos, reglas de negocio, actores, estructura organizacional, eventos y tecnologías.</li>
		<li>Modelar la arquitectura de negocios de una organización empleando lenguajes y herramientas apropiadas.</li>
		<li>Modelar, analizar, diseñar, evaluar, transformar y gestionar procesos de negocio.</li>
		<li>Identificar, analizar, especificar, documentar y validar los requisitos de un sistema o aplicación.</li>
		<li>Gestionar los requisitos de un sistema durante su desarrollo e implementación.</li>
	</ul>
	<br>
